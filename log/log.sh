#!/usr/bin/env bash

################################################################################
function log_info() {
    typeset msg=$1
    echo -e "\033[32m[INFO]\033[0m" "$msg"
}

################################################################################
function log_err() {
    typeset msg=$1
    echo -e "\033[31m[INFO]\033[0m" "$msg"

}

################################################################################
function log_warn() {
    typeset msg=$1
    echo -e "\033[33m[INFO]\033[0m" "$msg"
}
